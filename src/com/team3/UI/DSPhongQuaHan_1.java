    private void loadListRoomOverdue(){
        
        DefaultListModel model = new DefaultListModel();    
        model.removeAllElements();
        try {
            List<Model_PhieuDangKy> list = this.dkdao.selectbyOverduedRoom();
            if(list!=null){
            for (Model_PhieuDangKy pdk : list) {
                Model_Phong mdl = this.pDao.findById(String.valueOf(pdk.getMaPhong()));
                Model_KhachHang mdlkh = this.khdao.findById(pdk.getMaKH());
                model.addElement(mdl.getMaPhong()+"--"+mdl.getTenPhong()+"--"+mdlkh.getHoTen()+"--"+pdk.getNgayNhanPhong()+"--"+mdlkh.getSdt()+"--"+mdlkh.getEmail());
                lsOverdue.setModel(model);
                }  
            } else{
                this.dispose();
                DialogHelper.alert(this, "Không có phòng ở nào hết hạn!");  
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn CSDL!");
            e.printStackTrace();
        }
    }
    void extendDate(){
        int index = lsOverdue.getSelectedIndex();
        ListModel lModel = lsOverdue.getModel();
        String raw = lModel.getElementAt(index).toString().split("--")[0];
        if(index>=0){
        Model_Phong mdlp = this.pDao.findById(raw);
        PhieuDangKy_Form jd = new PhieuDangKy_Form (new javax.swing.JFrame(), true,mdlp.getMaPhong(),mdlp.getTenPhong(),mdlp.getTrangThai());
                jd.setVisible(true);
                jd.setModal(true);
                jd.setAlwaysOnTop(true);
                jd.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
        loadListRoomOverdue();  
        } else{
            DialogHelper.alert(this, "Bạn chưa chọn phòng cần gia hạn!");
        }   
    }
