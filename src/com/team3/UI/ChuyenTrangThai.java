    void update(){
        Model_Phong mdl = this.pDAO.findById(maPhong);
        
        if(rdoTrong.isSelected()){
            mdl.setTrangThai(0);
        } else if(rdoBan.isSelected()){
            mdl.setTrangThai(2);
        }else if(rdoSua.isSelected()){
            mdl.setTrangThai(4);
        }      
        try {
            pDAO.update(mdl);                       
            DialogHelper.alert(this, "Cập nhật trạng thái thành công!"); 
            this.dispose();
            SoDoPhong.btnShowAll.doClick();
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật trạng thái thất bại!");
            e.printStackTrace();
        }
    }
