    void register() {
        try {
            String pass = new String(txtPassword.getPassword());
            String confirm = new String(txtRePassword.getPassword());
            Model_NhanVien model = getModel();
            Model_NhanVien model2 = nvd.findByEmail(txtEmail.getText());
            Model_NhanVien model3 = nvd.findById(txtUserName.getText());
            String ho = txtHo.getText();
            String ten = txtTen.getText();
            String email = txtEmail.getText();
            String regex = "^[\\w-_\\.[^0-9]]+\\@[\\w&&[a-z][^0-9]]+\\.[\\w&&[a-z][^0-9]]+[\\.[a-z]]+$";
            if(txtUserName.getText().length()==0){
                DialogHelper.alert(this, "Không được bỏ trống Tên đăng nhập");
                txtUserName.requestFocus();
            }else if(model3 != null){
                DialogHelper.alert(this, "Tài khoản đã tồn tại");
                txtUserName.requestFocus();
            } else if(pass.length()<5){
                DialogHelper.alert(this, "Mật khẩu phải lớn hơn 5 kí tự");
                txtPassword.requestFocus();
            } else if(!confirm.equals(pass)){
                DialogHelper.alert(this, "Xác nhận mật khẩu không đúng!");
                txtRePassword.requestFocus();
            } else if(ho.chars().allMatch(Character::isDigit)==true 
                    &&ho.length()==0
                    ||ten.chars().allMatch(Character::isDigit)==true
                    &&ten.length()==0
            ){
                DialogHelper.alert(this, "Họ Tên phải là chữ cái và không được bỏ trống!");
                txtHo.requestFocus();
            } else if(!email.matches(regex)||email.length()==0){
                DialogHelper.alert(this, "Email không đúng dạng!");
                txtEmail.requestFocus();
            } else if(model2!=null){
                DialogHelper.alert(this, "Email đã có người đăng kí!");
                txtEmail.requestFocus();                     
            } else if (!chkConfirm.isSelected()){
                    DialogHelper.alert(this, "Bạn chưa đồng ý với điều khoản");
            } else{
                nvd.insert(model);
                DialogHelper.alert(this, "Đăng kí thành công!!");
                this.openSignIn();
                
            }                 
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
