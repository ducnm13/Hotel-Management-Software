    Model_NhanVien getModel() {
        try {
            String pass = new String(txtPassword.getPassword());
            Model_NhanVien model = new Model_NhanVien();
            model.setMaNV(txtUserName.getText());
            model.setMatKhau(pass);
            model.setHo(txtHo.getText());
            model.setTen(txtTen.getText());
            model.setEmail(txtEmail.getText());
            model.setDiaChi("0");
            model.setSDT("0");
            model.setGioiTinh(true);
            model.setHinh("0");
            model.setVaiTro(0);
            return model;
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

