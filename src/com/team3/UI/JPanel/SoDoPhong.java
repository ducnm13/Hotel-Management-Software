     private void updateRoomOverdue(){
        try {
            List<Model_PhieuDangKy> list = this.dkdao.selectbyOverdueRoom();
            if(list!=null){
            for (Model_PhieuDangKy pdk : list) {
                Model_Phong mdl = this.dao.findById(String.valueOf(pdk.getMaPhong()));
                mdl.setTrangThai(3);
                this.dao.update(mdl);
                }  
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn CSDL!");
            e.printStackTrace();
        }
    }
