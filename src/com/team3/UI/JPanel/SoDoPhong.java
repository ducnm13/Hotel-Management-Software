    // Load Blocks
    public void loadBlock(){
        BlockItems.removeAll();
        List<Model_Phong> list = this.dao.select();
        
        for (Model_Phong mdl:list){ 
            String ten="";
            String ngayNhan="";
            String ngayTra="";
            String maPhong = mdl.getMaPhong();
            String tenPhong = mdl.getTenPhong();
            int trangThai = mdl.getTrangThai();
            int maLoaiPhong = Integer.parseInt(mdl.getMaLoaiPhong());
            Model_PhieuDangKy listDK = this.dkdao.findByMaPhongActive3(mdl.getMaPhong());
            if(listDK!=null){
            int maKH = listDK.getMaKH();
            Model_KhachHang listKH = this.khdao.findById(maKH);
            ten = listKH.getHoTen();
            ngayNhan = DateHelper.toString(listDK.getNgayNhanPhong(), new String[0]);
            ngayTra = String.valueOf(DateHelper.toString(listDK.getNgayTraPhongDuKien(), new String[0]));
          
            }
            BlockItems.add(new BlockPhong(maPhong,tenPhong,trangThai,maLoaiPhong,ten,ngayNhan,ngayTra));
            BlockItems.repaint();
            BlockItems.revalidate();
            
        }
    }
    
