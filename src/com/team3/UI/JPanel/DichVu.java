    void load() {
        DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();
        model.setRowCount(0);
        try {
            List<Model_DichVu> list = dao.select();
            for (Model_DichVu dv : list) {
                Object[] row = new Object[]{
                    dv.getMadv(), dv.getTen(), (int)dv.getDongia()
                };
                model.addRow(row);
            }
            if (list.size() > 0) {
                tblDichVu.setRowSelectionInterval(index, index);
            }
            txtTenDV.requestFocus();
            setButton(true);
        } catch (Exception e) {

        }
    }

    void clear() {
        txtTenDV.setText("");
        txtDonGia.setText("");
        index = 0;
        load();
    }

    void insert() {
        try {
            Model_DichVu model = getModel();
            dao.inset(model);
            clear();
            load();
            DialogHelper.alert(this, "Thêm thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Thêm không thành công");
        }
    }

    void update() {
        try {
            Model_DichVu model = getModel();
            dao.update(model);
            clear();
            load();
            DialogHelper.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật không thành công");
        }
    }

    void delete() {
        try {
            Model_DichVu model = getModel();
            dao.delete(model.getMadv());
            clear();
            load();
            DialogHelper.alert(this, "Xoá thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Xoá không thành công");
        }

    }

    Model_DichVu getModel() {
        Model_DichVu model = new Model_DichVu();
        model.setMadv((int) tblDichVu.getValueAt(this.index, 0));
        model.setTen(txtTenDV.getText().trim());
        model.setDongia(Double.parseDouble(txtDonGia.getText().trim()));
        return model;
    }

    void setModel(Model_DichVu model) {
        txtTenDV.setText(model.getTen());
        txtDonGia.setText(String.valueOf(df.format(model.getDongia())));
    }

    void edit() {
        try {
            int id = (int) tblDichVu.getValueAt(this.index, 0);
            Model_DichVu model = dao.findById(id);
            setModel(model);
            setButton(false);
        } catch (Exception e) {

        }
    }

    void setButton(boolean value) {
        btnThem.setEnabled(value);
        btnXoa.setEnabled(!value);
        btnSua.setEnabled(!value);
    }

    boolean check() {
        if (checkLength() && checkTen() && checkGia()) {
            return true;
        } else {
            return false;
        }
    }

    boolean checkLength() {
        if (txtTenDV.getText().trim().length() > 0 && txtDonGia.getText().trim().length() > 0) {
            return true;
        } else {
            DialogHelper.alert(this, "Tất cả thông tin không được để trống");
            return false;
        }
    }

    boolean checkTen() {
        if (!txtTenDV.getText().trim().matches("^[a-zA-Z_ÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚĂĐĨŨƠàáâãèéêìíòóôõùúăđĩũơƯĂẠẢẤẦẨẪẬẮẰẲẴẶ"
                + "ẸẺẼỀỀỂưăạảấầẩẫậắằẳẵặẹẻẽềềểỄỆỈỊỌỎỐỒỔỖỘỚỜỞỠỢỤỦỨỪễệỉịọỏốồổỗộớờởỡợ"
                + "ụủứừỬỮỰỲỴÝỶỸửữựỳỵỷỹ\\s]+$") || txtTenDV.getText().trim().equals("")) {
            DialogHelper.alert(this, "Tên dịch vụ không được nhập số");
            txtTenDV.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    boolean checkGia() {
        String dongia = "[0-9]{1,100}";
        if (txtDonGia.getText().trim().matches(dongia)) {
            return true;
        } else {
            DialogHelper.alert(this, "Giá chỉ được nhập số");
            txtDonGia.requestFocus();
            return false;
        }
    }

    boolean checkRole() {
        if (ShareHelper.role == 2) {
            return true;
        } else {
            DialogHelper.alert(this, "Chỉ có quản lý mới được quyền thao tác");
            return false;
        }
    }
