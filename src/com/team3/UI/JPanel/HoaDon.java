    void init() {
        load();
        setButton(false);
        dccNgayThanhToan.setDateFormatString("dd/MM/yyyy");
        dccNgayThanhToan.setDate(new Date());
    }

    void load() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        try {
            List<Model_HoaDon> list = dao.select();
            for (Model_HoaDon hd : list) {
                Object[] row = new Object[]{
                    hd.getMaHD(),
                    hd.getMaPhieuDK(),
                    DateHelper.toString(hd.getNgayThanhToan(), "dd/MM/yyyy"),
                    StringParseHelper.HinhThucThanhToantoString(hd.getHinhThucThanhToan()),
                    hd.getTongTien(),
                    hd.getMaNV(),
                    StringParseHelper.TrangThaitoString(hd.getTrangThai())
                };
                model.addRow(row);
                if (model.getRowCount() > 0) {
                    tblHoaDon.setRowSelectionInterval(index, index);
                }
            }
        } catch (Exception e) {

        }
    }

    void clear() {
        txtMaPhieuDK.setText("");
        txtMaNV.setText("");
        txtHinhThuc.setText("");
        txtTongtien.setText("");
        txtTrangThai.setText("");
        dccNgayThanhToan.setDate(DateHelper.now());
        index = 0;
        setButton(false);
    }

    void delete() {
        try {
            int mahd = (int) tblHoaDon.getValueAt(this.index, 0);
            dao.delete(mahd);
            clear();
            load();
            DialogHelper.alert(this, "Xoá thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Xoá không thành công");
        }
    }

    void setModel(Model_HoaDon model) {
        txtMaPhieuDK.setText(String.valueOf(model.getMaPhieuDK()));
        dccNgayThanhToan.setDate(model.getNgayThanhToan());
        txtHinhThuc.setText(StringParseHelper.HinhThucThanhToantoString(model.getHinhThucThanhToan()));
        txtTongtien.setText(String.valueOf(df.format(model.getTongTien())));
        txtMaNV.setText(model.getMaNV());
        txtTrangThai.setText(StringParseHelper.TrangThaitoString(model.getTrangThai()));
    }

    void edit() {
        try {
            int mahd = (int) tblHoaDon.getValueAt(this.index, 0);
            Model_HoaDon model = dao.findById(String.valueOf(mahd));
            if (model != null) {
                setModel(model);
                setButton(true);
            }
        } catch (Exception e) {

        }
    }

    void setButton(boolean value) {
        btnXoa.setEnabled(value);
        btnSua.setEnabled(value);
    }

    boolean checkTrangThai() {
        int mahd = (int) tblHoaDon.getValueAt(this.index, 0);
        Model_HoaDon model = dao.findById(String.valueOf(mahd));
        if (model != null) {
            if (model.getTrangThai() == 0) {
                DialogHelper.alert(this, "Chỉ được xoá hoá đơn đã thanh toán");
                return false;
            } else {
                return true;
            }
        } else {
            return false;
        }
    }

    boolean checkRole() {
        if (ShareHelper.role == 2) {
            return true;
        } else {
            DialogHelper.alert(this, "Chỉ quản lý mới được quyền thao tác");
            return false;
        }
    }
    public void exportExcel(JTable table) {
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileNameExtensionFilter("Microsoft 97/2003 Worksheet Book - *.xls ","xls"));
        int i = chooser.showSaveDialog(chooser);
        if (i == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            try {
                FileWriter out = new FileWriter(file+".xls");
                BufferedWriter bwrite = new BufferedWriter(out);
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                // ten Cot
                for (int j = 0; j < table.getColumnCount(); j++) {
                    bwrite.write(model.getColumnName(j) + "\t");
                }
                bwrite.write("\n");
                // Lay du lieu dong
                for (int j = 0; j < table.getRowCount(); j++) {
                    for (int k = 0; k < table.getColumnCount(); k++) {
                        bwrite.write(model.getValueAt(j, k) + "\t");
                    }
                    bwrite.write("\n");
                }
                bwrite.close();
                DialogHelper.alert(this, "Trích xuất thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Lỗi trích xuất bảng!");
            }
        }
    }
