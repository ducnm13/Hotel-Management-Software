    void load() {
        DefaultTableModel model = (DefaultTableModel) tblLoaiPhong.getModel();
        model.setRowCount(0);
        try {
            List<Model_LoaiPhong> list = dao.select();
            for (Model_LoaiPhong lp : list) {
                Object[] row = new Object[]{
                    lp.getMaLP(), lp.getTenLP(), (int) lp.getDonGia(), lp.getSoNguoi(), lp.getHinh()
                };
                model.addRow(row);
            }
            if (list.size() > 0) {
                tblLoaiPhong.setRowSelectionInterval(index, index);
            }
            txtTenLP.requestFocus();
            setButton(true);
        } catch (Exception e) {

        }
    }

    void clear() {
        txtTenLP.setText("");
        txtDonGia.setText("");
        txtSoNguoi.setText("");
        lblHinhAnh.setIcon(null);
        index = 0;
        load();
        setButton(true);
        txtTenLP.setEnabled(true);
    }

    void insert() {
        try {
            Model_LoaiPhong model = getModel();
            dao.insert(model);
            clear();
            load();
            DialogHelper.alert(this, "Thêm thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Thêm không thành công");
        }
    }

    void update() {
        try {
            Model_LoaiPhong model = getModel();
            dao.update(model);
            clear();
            load();
            DialogHelper.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật không thành công");
        }
    }

    void delete() {
        try {
            int id = (int) tblLoaiPhong.getValueAt(this.index, 0);
            dao.delete(String.valueOf(id));
            clear();
            load();
            DialogHelper.alert(this, "Xoá thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Xoá không thành công");
        }
    }

    Model_LoaiPhong getModel() {
        Model_LoaiPhong model = new Model_LoaiPhong();
        model.setMaLP((int) tblLoaiPhong.getValueAt(this.index, 0));
        model.setTenLP(txtTenLP.getText());
        model.setDonGia(Integer.parseInt(txtDonGia.getText()));
        model.setSoNguoi(Integer.parseInt(txtSoNguoi.getText()));
        model.setHinh(lblHinhAnh.getToolTipText());
        return model;
    }

    void setModel(Model_LoaiPhong model) {
        txtTenLP.setText(model.getTenLP());
        txtDonGia.setText(String.valueOf(df.format(model.getDonGia())));
        txtSoNguoi.setText(String.valueOf(model.getSoNguoi()));
        if (model.getHinh() != null) {
            Image im = ShareHelper.readLogo(model.getHinh()).getImage();
            ImageIcon icon = new ImageIcon(im.getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getHeight(), im.SCALE_SMOOTH));
            lblHinhAnh.setIcon(icon);
        } else {
            lblHinhAnh.setIcon(null);
        }
    }

    void edit() {
        try {
            int id = (int) tblLoaiPhong.getValueAt(this.index, 0);
            String oldimg = (String) tblLoaiPhong.getValueAt(this.index, 4);
            Model_LoaiPhong model = dao.findById(String.valueOf(id));
            if (model != null) {
                setModel(model);
                setButton(false);
                txtTenLP.setEnabled(false);
                lblHinhAnh.setToolTipText(oldimg);
            }
        } catch (Exception e) {

        }
    }

    void setButton(boolean value) {
        btnThem.setEnabled(value);
        btnSua.setEnabled(!value);
        btnXoa.setEnabled(!value);
    }

    void UploadImage() {
        JFileChooser jfile = new JFileChooser();
        jfile.setDialogTitle("Chọn hình loại phòng");
        if (jfile.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = jfile.getSelectedFile();
            if (ShareHelper.savelogo(file)) {
                Image im = ShareHelper.readLogo(file.getName()).getImage();
                ImageIcon icon = new ImageIcon(im.getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getHeight(), im.SCALE_SMOOTH));
                lblHinhAnh.setIcon(icon);
                lblHinhAnh.setToolTipText(file.getName());
            }
        }
    }

    void notice() {
        txtTenLP.setToolTipText("Chỉ được nhập chữ");
        txtDonGia.setToolTipText("Chỉ được nhập số");
        txtSoNguoi.setToolTipText("Chỉ được nhập số");
    }

    boolean check() {
        if (checkLength() && checkTenLP() && checkDongia() && checkSonguoi()) {
            return true;
        } else {
            return false;
        }
    }

    boolean checkLength() {
        if (txtTenLP.getText().length() > 0 && txtDonGia.getText().length() > 0 && txtSoNguoi.getText().length() > 0) {
            return true;
        } else {
            DialogHelper.alert(this, "Tất cả thông tin loại phòng không được để trống");
            return false;
        }
    }

    boolean checkDongia() {
        String dongia = "[0-9]{1,100}";
        if (txtDonGia.getText().trim().matches(dongia)) {
            return true;
        } else {
            DialogHelper.alert(this, "Giá chỉ được nhập số");
            txtDonGia.requestFocus();
            return false;
        }
    }

    boolean checkSonguoi() {
        String songuoi = "[0-9]{1,100}";
        if (txtSoNguoi.getText().trim().matches(songuoi)) {
            return true;
        } else {
            DialogHelper.alert(this, "Số người chỉ được nhập số");
            txtSoNguoi.requestFocus();
            return false;
        }
    }

