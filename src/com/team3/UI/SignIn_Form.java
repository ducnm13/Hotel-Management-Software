    private void btnViewPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewPassActionPerformed
        if (btnViewPass.isSelected()) {
            btnViewPass.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/team3/Images/icons/hideeyes.png")));
            txtPassword.setEchoChar((char) 0);
        } else {
            btnViewPass.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/team3/Images/icons/eyes.png")));
            txtPassword.setEchoChar('•');
        }
    }//GEN-LAST:event_btnViewPassActionPerformed

    private void txtPasswordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyTyped
        String pass = new String(txtPassword.getPassword());
        if (pass.length() > 0) {
            btnViewPass.setVisible(true);
            btnViewPass.setEnabled(true);
        } else if (pass.length() == 0) {
            btnViewPass.setVisible(false);
            btnViewPass.setEnabled(false);
        }
    }//GEN-LAST:event_txtPasswordKeyTyped

    private void txtPasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            this.login();
        }
        if (evt.getKeyCode() == KeyEvent.VK_UP) {
            txtUserName.requestFocus();
        }
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            btnLogin.requestFocus();
        }
    }//GEN-LAST:event_txtPasswordKeyPressed

